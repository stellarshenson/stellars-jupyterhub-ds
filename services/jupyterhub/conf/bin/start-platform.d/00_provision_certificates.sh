#!/bin/bash
# Two-stage cert provisioning for Traefik: operator -> persisted -> auto.
#
#   /mnt/user_certs   operator-supplied yml + cert/key (optional bind-mount)
#   /mnt/certs        runtime dir Traefik scans (directory provider + watch=true)
#
# Decision: operator if /mnt/user_certs has at least one *.yml/*.yaml AND
# every certFile/keyFile/caFile referenced by those yml files resolves to an
# existing file under /mnt/user_certs (or another extant absolute path).
# Otherwise persisted (anything already in /mnt/certs from a prior boot).
# Otherwise auto-generate via /mkcert.sh.

set -e

USER_CERTS="${CERTIFICATE_USER_CERTS_DIR:-/mnt/user_certs}"
TARGET="${CERTIFICATE_TARGET_DIR:-/mnt/certs}"
DEFAULT_CN="${CERTIFICATE_DOMAIN_NAME:-localhost}"
LOG_PREFIX="[Certificates]"

shopt -s nullglob
mkdir -p "$TARGET"

log()      { echo "$LOG_PREFIX $*"; }
log_warn() { echo "$LOG_PREFIX WARN: $*" >&2; }
log_err()  { echo "$LOG_PREFIX ERROR: $*" >&2; }

# Pull every certFile / keyFile / caFile out of a yml, recursively.
extract_paths() {
    local yml="$1"
    yq eval '.. | (.certFile? , .keyFile? , .caFile?) | select(. != null)' "$yml" 2>/dev/null
}

# Normalise an extracted path to where it should live inside /mnt/user_certs
# while we're still validating; absolute-elsewhere paths pass through.
resolve_user_path() {
    case "$1" in
        "$USER_CERTS"/*) echo "$1" ;;
        "$TARGET"/*)     echo "$USER_CERTS/${1#$TARGET/}" ;;
        /*)              echo "$1" ;;            # other absolute - operator's own path
        *)               echo "$USER_CERTS/$1" ;; # relative -> under user_certs/
    esac
}

operator_certs_valid() {
    [ -d "$USER_CERTS" ] || return 1
    local ymls=("$USER_CERTS"/*.yml "$USER_CERTS"/*.yaml)
    [ "${#ymls[@]}" -gt 0 ] || return 1
    local yml p resolved
    for yml in "${ymls[@]}"; do
        for p in $(extract_paths "$yml"); do
            resolved=$(resolve_user_path "$p")
            if [ ! -f "$resolved" ]; then
                log_err "operator yml $yml references $p but $resolved is missing"
                return 1
            fi
        done
    done
    return 0
}

apply_operator_certs() {
    log "applying operator certs from $USER_CERTS"
    find "$TARGET" -maxdepth 1 -type f \
        \( -name '*.crt' -o -name '*.key' -o -name '*.pem' -o -name '*.yml' -o -name '*.yaml' \) \
        -delete
    cp -a "$USER_CERTS"/. "$TARGET"/
    # Rewrite path prefix in any copied yml so references resolve at the new location.
    find "$TARGET" -maxdepth 1 -type f \( -name '*.yml' -o -name '*.yaml' \) -exec \
        sed -i "s|${USER_CERTS}/|${TARGET}/|g" {} \;
}

persisted_certs_valid() {
    local crts=("$TARGET"/*.crt)
    local ymls=("$TARGET"/*.yml "$TARGET"/*.yaml)
    [ "${#crts[@]}" -gt 0 ] && [ "${#ymls[@]}" -gt 0 ]
}

auto_generate() {
    log "auto-generating self-signed cert (CN=$DEFAULT_CN)"
    /mkcert.sh "$TARGET" "$DEFAULT_CN" "server"
    cat > "$TARGET/certs.yml" <<EOF
# auto-generated by 00_provision_certificates.sh
tls:
  stores:
    default:
      defaultCertificate:
        certFile: $TARGET/server.crt
        keyFile:  $TARGET/server.key
EOF
}

print_status() {
    local source="$1"
    echo "$LOG_PREFIX ========================================================"
    log "source: $source"
    local yml crt subj san issuer not_after
    for yml in "$TARGET"/*.yml "$TARGET"/*.yaml; do
        log "  yml:    $yml"
    done
    for crt in "$TARGET"/*.crt; do
        subj=$(openssl x509 -noout -subject -in "$crt"  2>/dev/null | sed 's/^subject=//' || echo '?')
        issuer=$(openssl x509 -noout -issuer -in "$crt" 2>/dev/null | sed 's/^issuer=//'  || echo '?')
        not_after=$(openssl x509 -noout -enddate -in "$crt" 2>/dev/null | cut -d= -f2     || echo '?')
        san=$(openssl x509 -noout -ext subjectAltName -in "$crt" 2>/dev/null \
              | awk '/X509v3 Subject Alternative Name/{getline; sub(/^ *+/,""); print}')
        log "  cert:   $crt"
        log "    subject: $subj"
        log "    SAN:     ${san:-<none>}"
        log "    issuer:  $issuer"
        log "    expires: $not_after"
    done
    echo "$LOG_PREFIX ========================================================"
}

if operator_certs_valid; then
    apply_operator_certs
    print_status "operator-supplied"
elif persisted_certs_valid; then
    log_warn "no valid operator certs in $USER_CERTS; using existing $TARGET"
    print_status "persisted"
else
    log_warn "no operator certs and no persisted certs; auto-generating"
    auto_generate
    print_status "auto-generated"
fi

# EOF
